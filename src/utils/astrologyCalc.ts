/**
 * 计算每周运势
 */
function calcWeeklyFortune(sign: AstrologySign) {
  const now = new Date();
  const weekNum = getWeekNumber(now);
  const signOffset = signOffsets[sign.name] || 1;
  const weekSeed = now.getFullYear() * 100 + weekNum + signOffset;

  const element = sign.element;
  const pick = (arr: string[], offset: number): string => {
    const idx = Math.floor(seededRandom(weekSeed + offset) * arr.length);
    return arr[idx];
  };

  return {
    overall: pick(weeklyOverall[element], 0),
    career: pick(weeklyCareer[element], 10),
    love: pick(weeklyLove[element], 20),
    wealth: pick(weeklyWealth[element], 30),
    advice: pick(weeklyAdvice[element], 40),
  };
}

