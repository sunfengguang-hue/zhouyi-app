import type { HexagramData, LineInfo, TrigramName } from '../types';

// 八卦爻型 (从下到上, true=阳 false=阴)
const T: Record<TrigramName, [boolean, boolean, boolean]> = {
  '乾': [true, true, true],
  '兑': [true, true, false],
  '离': [true, false, true],
  '震': [true, false, false],
  '巽': [false, true, true],
  '坎': [false, true, false],
  '艮': [false, false, true],
  '坤': [false, false, false],
};

const posNames = ['初', '二', '三', '四', '五', '上'];

function mkLines(
  upper: TrigramName, lower: TrigramName,
  yaos: string[], yaoT: string[]
): LineInfo[] {
  const isYang = [...T[lower], ...T[upper]];
  return isYang.map((yang, i) => ({
    position: i + 1,
    value: yang ? 7 : 8,
    type: (yang ? 'yang' : 'yin') as LineInfo['type'],
    change: 'unchanged' as const,
    name: `${yang ? '九' : '六'}${posNames[i]}`,
    text: yaos[i] || '',
    translation: yaoT[i] || '',
  }));
}

function mk(
  num: number, name: string, full: string,
  upper: TrigramName, lower: TrigramName,
  j: string, jt: string, img: string, imgT: string,
  com: string, comT: string,
  yaos: string[], yaoT: string[],
  career: string, love: string, wealth: string, health: string
): HexagramData {
  return {
    number: num, name, fullName: full,
    upperTrigram: upper, lowerTrigram: lower,
    judgment: j, judgmentTranslation: jt,
    image: img, imageTranslation: imgT,
    commentary: com, commentaryTranslation: comT,
    lines: mkLines(upper, lower, yaos, yaoT),
    interpretation: { career, love, wealth, health },
  };
}

