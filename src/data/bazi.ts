// ==================== 十神关系 ====================
// 以日干为"我"，根据天干间的五行生克与阴阳关系定十神：
// 同我 = 比劫（阴阳同=比肩，阴阳异=劫财）
// 生我 = 印枭（阴阳同=偏印，阴阳异=正印）
// 我生 = 食伤（阴阳同=食神，阴阳异=伤官）
// 克我 = 官杀（阴阳同=七杀，阴阳异=正官）
// 我克 = 财星（阴阳同=偏财，阴阳异=正财）
//
// 五行相生：木→火→土→金→水→木
// 五行相克：木→土→水→火→金→木
const WX_SHENG: Record<string, string> = { '木':'火', '火':'土', '土':'金', '金':'水', '水':'木' };
const WX_KE: Record<string, string> = { '木':'土', '火':'金', '土':'水', '金':'木', '水':'火' };

export function getShiShen(dayGan: string, targetGan: string): string {
  const dWX = TIAN_GAN_WUXING[dayGan];
  const tWX = TIAN_GAN_WUXING[targetGan];
  const dYY = TIAN_GAN_YINYANG[dayGan];
  const tYY = TIAN_GAN_YINYANG[targetGan];
  const sameYY = dYY === tYY; // 同阴阳

  if (dWX === tWX) return sameYY ? '比肩' : '劫财';
  if (WX_SHENG[dWX] === tWX) return sameYY ? '食神' : '伤官';  // 我生
  if (WX_KE[dWX] === tWX) return sameYY ? '偏财' : '正财';   // 我克
  if (WX_KE[tWX] === dWX) return sameYY ? '七杀' : '正官';   // 克我
  if (WX_SHENG[tWX] === dWX) return sameYY ? '偏印' : '正印'; // 生我
  return '比肩'; // fallback
}

